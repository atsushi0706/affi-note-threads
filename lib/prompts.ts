// =============================================================
// affi-note-threads プロンプト定義
// 既存の note-writer-tool / threads-writer-tool の理屈を踏襲しつつ、
// 「アフィリエイター（紹介する第三者）」の立ち位置に作り替えたもの。
//
// チェーン: ①LP分析 → ②Web検索リサーチ(grounding) → ③モード別生成
// 一撃生成は禁止。Rule of One と ONE HACK を踏襲。
// =============================================================

export type Analysis = {
  target: string; // 想定読者・ターゲット像
  painPoints: string[]; // 読者の悩み・課題
  benefits: string[]; // LP/オファーが提供する価値
  uniqueAngle: string; // この案件ならではの切り口・独自性
  goal: string; // 最終誘導ゴール（例: 無料セミナー登録）
  tone: string; // 文体・トーンの方向性
  genre: string; // ジャンル（ビジネス/心理/健康/スピリチュアル/日常 等）
  searchKeywords: string[]; // Threadsで「〇〇で検索」に使える語句候補
};

export type Research = {
  evidence: { title: string; summary: string; source: string }[];
  expertQuotes: { expert: string; quote: string; context: string }[];
  keyInsight: string; // 記事の核となる洞察
  oneIdea: string; // 伝える1つのアイデア
  oneEmotion: string; // 揺さぶる1つの感情
  oneAction: string; // 促す1つの行動
};

export type GenerateMode = "note" | "threads" | "threads-pin" | "angles";

// 生成前に提示する「方向性」案
export type Angle = {
  id: number;
  theme: string; // 方向性のテーマ（一言）
  angle: string; // 切り口の説明
  whoFor: string; // 誰向け（職業・状況）
  value: string; // 読者が得る価値
};

export type Options = {
  lpUrl?: string; // 誘導先LPのURL
  chosenAngle?: Angle; // 選択された方向性（あれば最優先で反映）
  // 書き手の人格・トーンを注入する任意項目。書くほどAIがその人に寄る
  persona?: string; // あなたは何者か（立場・プロフィール）
  voiceSamples?: string; // 普段の言葉遣い・文体サンプル（雰囲気の参考）
  // 毎回アウトプットを変えるための「今日の方向性」（生成のたびにランダムで振る）
  today?: string; // 今日の日付（例: 2026年6月30日）
  timeframe?: string; // 時間軸（過去/今/トレンド）を文章化したもの
  angleSeed?: string; // 今日の切り口のヒント（ランダムに選ばれた種）
};

// ── 立ち位置（全モード共通・最重要）─────────────────
// アフィリエイター＝「紹介する第三者」。LP執筆者本人になりきらせない。
function writerStance(): string {
  return `# あなたは誰か（最重要）
あなたは、マイケル・マスターソン（Michael Masterson）のダイレクトレスポンス・コピーライティングを日本人向けにアレンジして20年以上書いてきた、プロのコピーライターです。
ある無料オファー(LP)を読者に"紹介する"立場ですが、これは【内部設定】です。
- 「アフィリエイターの〇〇です」とは名乗らない。肩書き・実績をわざとらしく出さない。
- LPを書いた本人になりきらない（「私が開発した」等はNG）。LPの売り文句をそのまま転載しない。
- 売り込みは本文でしない。誘導は本文と切り離した"案内"に置く（後述）。

# プロの設計：書く前に「何を・誰に・どの順番で」を必ず決める（最重要）
※「どう言うか」（表現・言い回し）は本質ではない。プロは"届け方の設計"で質を出す。
- 誰に(Who): たった1人の読者を定める。その人が今どの段階にいるか（問題にまだ気づいていない／気づいている／解決策を探している）を見極め、それに合わせる。
- 何を(What): 【One Big Idea＝この記事/投稿で伝える、たった1つの大きな核心】を、リサーチで調べた具体的な事実・最新動向・データ・実例から立てる。約束(promise)は具体的に。
- どの順番で(Order): 読者の段階に合った説得の流れで構成する。例) 関心を掴む → なぜ今これが重要か → 調べた事実・証拠で納得させる → 具体的にどう活きるか → 小さな次の一歩。
- マスターソンの「4つのU」を効かせる: Useful(役立つ)／Urgent(今これを知る理由)／Unique(独自の切り口)／Ultra-specific(超具体的)。
- One Big Idea の中身は、リサーチした具体（固有名詞・数字・実例・最新動向）で支える。一般論で薄く埋めない。

# 読者に"今すぐ使えるもの"をプレゼントする（最重要・これが信頼を生む）
- 情報を「教える・説明する」だけでは足りない。読者が【今日すぐできる具体的なもの】を手渡す（プレゼントする）。
- リサーチで調べたアメリカの最新の具体的なツール・使い方・ノウハウを、「例えば海外で○○というツールが出た。これは△△ができる。あなたの□□に、こう使える。試してみて」と、具体的なツール名・手順つきで手渡す。
- 抽象的な未来予測（"AIが時間の壁を破壊する"）や、一般論の説明（"複数のエージェントが連携する"）は中身ではない。具体的なツール名・今すぐできる手順を出す。
- どの分野でも同じ。海外(アメリカ)の最新ノウハウを調べ、読者がその場で動ける形に翻訳して贈る。`;
}

// ── 分析結果を文脈として埋め込む ─────────────────
function ctx(a: Analysis): string {
  return `# この案件の分析（紹介の土台。そのまま転載はしない）
- ジャンル: ${a.genre}
- ターゲット読者: ${a.target}
- 読者の悩み: ${a.painPoints.join(" / ")}
- この案件が提供する価値: ${a.benefits.join(" / ")}
- 独自の切り口: ${a.uniqueAngle}
- 誘導ゴール: ${a.goal}
- トーンの方向性: ${a.tone}`;
}

// ── ジャンル別の書き方ルール ─────────────────────
function genreStyle(a: Analysis): string {
  const g = (a.genre || "").toLowerCase();
  const biz = /ai|エージェント|ビジネス|副業|ツール|マーケ|集客|稼|投資|物販|seo|saas|自動化/.test(g);
  const mental = /心理|メンタル|健康|スピ|セラピ|ヒーリング|自己肯定|不安|うつ|ストレス|発達|hsp/.test(g);
  if (biz) {
    return `# このジャンル(${a.genre})の書き方
- 最近の動向・実例をリサーチ素材から取り入れ、古い一般論にしない。
- 抽象論で終わらせず、必ず【具体的な取り入れ方】を見せる。次のいずれかの型で:
  ・「〇〇という職業/状況の人なら、こう取り入れられるかもしれません」
  ・「私はこう使っています」（紹介者の一人称体験として）
  ・「こういう使い方をしている人たちがいます」（事例紹介）
- 読者が自分の状況に当てはめてイメージできるようにする。`;
  }
  if (mental) {
    return `# このジャンル(${a.genre})の書き方（最重要・厳守）
- 海外の論文・エビデンス・専門家の知見を根拠にしてよい。ただし:
  1. 小難しい言葉では絶対に書かない。
  2. 専門用語を使うときは必ず日常語に言い換える（例:「自己効力感（＝"自分はできる"という感覚）」）。
  3. 中学生でも分かる、誰にでも伝わる表現にする。
- 研究の権威で脅さない。読者に寄り添い、生活の場面に落として語る。`;
  }
  return "";
}

// ── 選択された方向性を文脈化 ─────────────────────
function angleContext(o: Options): string {
  const c = o.chosenAngle;
  if (!c) return "";
  return `# 今回書く方向性（これに沿って書く・最優先）
- テーマ: ${c.theme}
- 切り口: ${c.angle}
- 誰向け: ${c.whoFor}
- 提供価値: ${c.value}
※ この方向性を軸にする。LPの案件には"一部"つなげるが、LP全体の要約や宣伝にはしない。`;
}

// ── リサーチ素材を文脈化（grounding結果）──────────
function researchContext(r?: Research): string {
  if (!r) return "";
  const ev = r.evidence?.length
    ? r.evidence.map((e, i) => `${i + 1}. ${e.title} — ${e.summary}（出典: ${e.source}）`).join("\n")
    : "（なし）";
  const eq = r.expertQuotes?.length
    ? r.expertQuotes.map((q) => `- ${q.expert}:「${q.quote}」（${q.context}）`).join("\n")
    : "（なし）";
  return `# リサーチ素材（Web検索で集めた実在の情報。捏造はしない／使う場合は自然に）
## エビデンス
${ev}
## 専門家・著名人の言葉
${eq}
## 核の洞察
${r.keyInsight || "（なし）"}
- 伝える1アイデア: ${r.oneIdea || ""}
- 揺さぶる1感情: ${r.oneEmotion || ""}
- 促す1行動: ${r.oneAction || ""}`;
}

// ── 書き手の人格・思い・文体を注入 ───────────────
function writerContext(o: Options): string {
  const parts: string[] = [];
  if (o.persona?.trim()) parts.push(`- 何者か（立場・プロフィール）: ${o.persona.trim()}`);
  if (o.voiceSamples?.trim()) {
    parts.push(`- 普段の言葉遣い・文体サンプル（語り口の"雰囲気"だけを参考にする）:\n"""\n${o.voiceSamples.trim()}\n"""`);
  }
  if (!parts.length) return "";
  return `# 書き手について（この人格・立場・語り口で書く）
${parts.join("\n")}

【重要】上の書き手として書く。文体サンプルは"雰囲気"の参考であり、決まり文句やフレーズをそのままコピーしない。
同じ言い回しを毎回使わず、表現は都度変えて、その人が自分の言葉で書いたように仕上げる。`;
}

// ── 「今日の方向性」を注入（毎回ランダムで振る）───
function dailyDirection(o: Options): string {
  const parts: string[] = [];
  if (o.today?.trim()) parts.push(`- 今はだいたい ${o.today.trim()} 頃（※内部メモ。季節の空気感を意識するためだけに使い、日付や「今年も半分」等の時間表現は本文に書かない）`);
  if (o.timeframe?.trim()) parts.push(`- 切り口の時間軸: ${o.timeframe.trim()}`);
  if (o.angleSeed?.trim()) parts.push(`- 今日の切り口のヒント: ${o.angleSeed.trim()}`);
  if (!parts.length) return "";
  return `# 今日の方向性（毎回必ず変える・ヒントは"内部メモ"）
${parts.join("\n")}
※ これは出発点のヒント。言葉をそのまま本文に書かない。ここから発想して昨日までと違う角度で書く。同じ素材でも切り口を必ずずらすこと。`;
}

